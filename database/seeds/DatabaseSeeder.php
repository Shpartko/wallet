    private function currency_dicts()
    {
        DB::table('currency_dicts')->delete();         
        DB::table('currency_dicts')->insert($this->get_currency_dict());
    }

    private function exchange_rates()
    {
        $currency_dict = $this->get_currency_dict();
        $rate = array();
        $date = date('Y-m-d H:i:s');
        foreach ($currency_dict as $key=>$currency){
            $rate[]=array(
                'currency_id'=>$key + 1,
                'update_date'=>$date,
                'rate'=> (1000 - rand(1, 999)) / 100
            );
        }
        
        DB::table('exchange_rates')->delete();
        for($i=-15; $i<15; $i++){
            foreach ($rate as &$cur){
                $cur['rate_date'] = date('Y-m-d', strtotime(($i >= 0 ? "+" : "") . $i . " days"));
                $cur['rate'] = round($cur['rate'] *(rand(90, 110) / 100), 5);
            }
            
            DB::table('exchange_rates')->insert($rate);
        }
    }

    private function transactions($tran_cnt, $top_client_id){
        $dict_cnt=count($this->get_currency_dict());
        
        DB::table('transactions')->delete();
        for($i=0; $i < $tran_cnt; $i++){
            $operation = (rand(1, 100) < 25) ? 'REFILL' : 'TRANSFER';
            $wallet_from = ($operation != 'REFILL' ? rand(1, $top_client_id): null);
            $wallet_to = rand(1, $top_client_id);
            
            if($wallet_from == $wallet_to && $operation != 'REFILL'){
                continue;
            }
            
            $currency_wallet_id = (rand(1, 100) < 50 || $operation == 'REFILL') ? $wallet_to : $wallet_from;
            $currency_id = $currency_wallet_id % $dict_cnt + 1;
            $date = date('Y-m-d H:i:s', strtotime("-" . rand(1, 10) . " days "
                    . rand(0, 23) . " hours " . rand(0, 59) . " minutes " . rand(0, 59) . " seconds"));
            DB::table('transactions')->insert(
                    array('wallet_id_from' => $wallet_from, 'wallet_id_to' => $wallet_to
                        , 'operation' => $operation, 'tran_date' => $date
                        , 'currency_id' => $currency_id, 'amount' => (rand(1, 1000) / 10)
                    )
                );
        }
    }
